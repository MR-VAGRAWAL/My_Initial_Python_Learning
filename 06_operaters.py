a = 7
b = 23
# Arithmetic Operaters
print("The value of a+b =",a+b)
print("The value of a-b =",a-b)
print("The value of a*b =",a*b)
print("The value of a/b =",a/b)
print("The value of a**b =",a**b)

# Assingment Operater

c = 17
d = 25
c += 3
print(c)
d -= 7
print(d)
c *= 0
print(c)
d /= 2
print(d)

# Comparison Operater
e = 97
f = 199
print(e>f)
print(e<f)
print(e!=f)
print(e>=f)
print(e<=f)

# Logical Operaters

bool1 = True
bool2 = False
print("The value of bool1 and bool2 is " , (bool1 and bool2))
print("The value of bool1 or bool2 is " , (bool1 or bool2))
print("The value of not bool2 is " , (not bool2))
